#include "libft.h"

void	ft_striter(char *s, void (*f)(char*))
{
	int i;
	int l;

	if (s != NULL && f != NULL)
	{
		l = ft_strlen(s);
		i = 0;
		while (i != l)
		{
			f(&s[i]);
			i++;
		}
	}
}
